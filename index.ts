function usecompoundoperator(){
    let x = 10;
console.log("initial x:",x);
x += 5;//add 5 to x
console.log("x after adding 5:", x);
x -= 1;
console.log("x after subtracting 1:", x);
x *= 2;
console.log("x after multiplying by 2:", x);
x /= 2;
console.log("x after dividing by 2:", x);
x %= 2;
console.log("x after taking remainder:", x);
x **= 2;
console.log("x after taking power:", x);
}
usecompoundoperator();